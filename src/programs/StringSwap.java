package programs;

public class StringSwap {

	public static void main(String[] args) {
		String a = "Hello";
		String b = "World";

		System.out.println("Strings before swap: a = " + a + " and b = " + b);
		//a = Hello and b = World
		System.out.println("Strings after swap: a = " + a + " and b = " + b);
		//a = World and b = Hello
	}
}
